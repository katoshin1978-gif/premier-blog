def main():
    config = load_config()
    wp_cfg = config.get("wordpress", {})
    base_url = os.environ.get("WP_URL", "").rstrip("/")
    ip_base_url, host_header = _resolve_to_ip(base_url)

    content_html = markdown_to_html(CONTENT)

    payload = {
        "title":    TITLE,
        "content":  content_html,
        "status":   "publish",       # 下書きではなく公開
        "excerpt":  META,
        "categories": [CATEGORY_ID],
    }

    headers = {**_get_auth_header(), **host_header, "Content-Type": "application/json"}
    resp = requests.post(
        f"{ip_base_url}/wp-json/wp/v2/posts",
        json=payload, headers=headers, timeout=30, verify=_SSL_VERIFY
    )
    resp.raise_for_status()
    data = resp.json()
    post_id = data["id"]
    post_url = data.get("link", "")
    print(f"投稿完了: ID={post_id}")
    print(f"URL: {post_url}")

    # アフィリエイトブロックを末尾に追加
    affiliate_html = _build_affiliate_html(CATEGORY_ID, TITLE)
    related = _fetch_related_posts(base_url, ip_base_url, host_header, CATEGORY_ID, post_id)
    from publisher import _build_related_html
    related_html = _build_related_html(related) if related else ""

    suffix = (affiliate_html or "") + (related_html or "")
    if suffix:
        requests.post(
            f"{ip_base_url}/wp-json/wp/v2/posts/{post_id}",
            json={"content": content_html + suffix},
            headers=headers, timeout=15, verify=_SSL_VERIFY
        )
        print("アフィリエイト・関連記事: 追加完了")

if __name__ == "__main__":
    main()
